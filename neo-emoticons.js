function NeoEmoticonify(
	text,
	basePath = "https://images.neopets.com/neoboards/smilies/"
) {
	NEO_EMOTICONS.forEach((neoEmoticon) => {
		text = text.replace(
			neoEmoticon[0],
			(i, match) => `<img src="${basePath + neoEmoticon[1]}.gif" />`
		);
	});
	return text;
}

function NeoEmoticonifyPage(
	basePath = "https://images.neopets.com/neoboards/smilies/"
) {
	window.addEventListener("load", () => NeoEmoticonifyPageNow(basePath));
}

function NeoEmoticonifyPageNow(
	basePath = "https://images.neopets.com/neoboards/smilies/"
) {
	let body = document.getElementsByTagName("body")[0].innerHTML;
	body = NeoEmoticonify(body, basePath);
	document.getElementsByTagName("body")[0].innerHTML = body;
}